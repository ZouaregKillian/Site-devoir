package fr.foreach.cours.first;

public class Sensei {
    int pourcentageDeVictoire;
    int pourcentageDeDefaite;
    int serieDeVictoires;
    int serieDeDefaites;

    public Sensei(int pourcentageDeVictoire, int pourcentageDeDefaite, int serieDeVictoires, int serieDeDefaites) {
        this.pourcentageDeVictoire = pourcentageDeVictoire;
        this.pourcentageDeDefaite = pourcentageDeDefaite;
        this.serieDeVictoires = serieDeVictoires;
        this.serieDeDefaites = serieDeDefaites;
    }
}

