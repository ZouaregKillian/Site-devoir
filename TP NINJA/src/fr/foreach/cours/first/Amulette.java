package fr.foreach.cours.first;

public class Amulette {
    int niveau;
    int numero;
    int categorie;
    int bonus;
    String description;
    String nom;

    public Amulette(int niveau, int numero, int categorie, int bonus, String description, String nom) {
        this.niveau = niveau;
        this.numero = numero;
        this.categorie = categorie;
        this.bonus = bonus;
        this.description = description;
        this.nom = nom;
    }
}
